#Counts amount of times a specific substring is found within larger string, in this case susbtring : 'bob'
s = 'azcbobobegghakl'
counter = 0
answer = 0

stringHolder =''

for counter in range(len(s)):
   stringHolder = s[counter:counter+3]
   if stringHolder =='bob':
        answer +=1
    
      
print ('Number of times bob occurs is: '+ str(answer))
